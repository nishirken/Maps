export function em(currentFontSize, rootFontSize = 16) {
  return `${rootFontSize / currentFontSize}em`;
}

export function rem(currentFontSize, rootFontSize = 16) {
  return `${rootFontSize / currentFontSize}em`;
}

export function per(currentFontSize, rootFontSize = 16) {
  return `${rootFontSize / currentFontSize}em`;
}
